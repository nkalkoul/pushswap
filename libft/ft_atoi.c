#include "libft.h"

int	ft_atoi(const char *s1)
{
	int	i;
	int	nbr;
	int	sign;

	sign = 1;
	i = 0;
	nbr = 0;
	while (((s1[i] >= 9 && s1[i] <= 13) || s1[i] == 32) && s1[i])
		i++;
	if (s1[i] == '-' || s1[i] == '+')
	{
		if (s1[i] == '-')
			sign = -1;
		i++;
	}
	while (s1[i] && ft_isdigit(s1[i]) == 1)
	{
		nbr = 10 * nbr + (s1[i] - 48);
		i++;
	}
	return (nbr * sign);
}
